create table aml_results (
   id              number generated by default as identity primary key,
   tx_id           varchar2(50),
   risk_score      number(5,2),
   sanctions_match varchar2(5),
   kyc_risk        varchar2(20),
   rule_hit        varchar2(100),
   ml_score        number(5,2),
   final_decision  varchar2(20),
   decision_reason varchar2(500),
   created_at      date default sysdate
);